const data = {
    text: 'default'
}
const input = document.getElementById('input');
const span = document.getElementById('span');
//数据劫持
Object.defineProperty(data, 'text', {
    set(newVal){
        input.value = newVal;
        span.innerHTML = newVal;
    }
})

input.addEventListener('keyup', e => {
    data.text = e.target.value;
})